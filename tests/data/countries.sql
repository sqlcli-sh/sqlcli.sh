CREATE TABLE countries_of_the_world (
    Country VARCHAR(255),
    Region VARCHAR(255),
    Population INT,
    Area_sq_mi FLOAT,
    Pop_Density_per_sq_mi FLOAT,
    Coastline_coast_area_ratio FLOAT,
    Net_migration FLOAT,
    Infant_mortality_per_1000_births FLOAT,
    GDP_per_capita FLOAT,
    Literacy_percent FLOAT,
    Phones_per_1000 FLOAT,
    Arable_percent FLOAT,
    Crops_percent FLOAT,
    Other_percent FLOAT,
    Climate VARCHAR(255),
    Birthrate FLOAT,
    Deathrate FLOAT,
    Agriculture FLOAT,
    Industry FLOAT,
    Service FLOAT
);